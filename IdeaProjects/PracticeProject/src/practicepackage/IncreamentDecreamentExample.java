package practicepackage;

public class IncreamentDecreamentExample {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(b);
        System.out.println(b++);
        System.out.println(++b);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(b--);
        System.out.println(--b);
        int c=50;
        int d=c++;
        System.out.println(d);
        d=++c;
        System.out.println(d);
        d=c--;
        System.out.println(d);
        d=--c;
        System.out.println(d);
    }
}
